def main():
    parser = argparse.ArgumentParser(
        description="Setup test data by copying source files to tests/fixtures/test_data/"
    )
    parser.add_argument(
        "--skip-r",
        action="store_true",
        help="Skip copying R-specific files (config.R, functions.R)",
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Overwrite existing files",
    )
    args = parser.parse_args()

    # Get the project root directory
    project_root = Path(__file__).parent.parent.parent
    test_data_dir = project_root / "tests" / "fixtures" / "test_data"

    print("=" * 70)
    print("Setup Test Data")
    print("=" * 70)
    print(f"Project root: {project_root}")
    print(f"Test data dir: {test_data_dir}")
    print(f"Skip R files: {args.skip_r}")
    print(f"Force overwrite: {args.force}")
    print("=" * 70)
    print()

    stats = {
        "r_files_copied": 0,
        "conf_files_copied": 0,
        "layer_files_copied": 0,
        "layer_csv_copied": False,
        "config_yaml_created": False,
    }

    # Copy R files if not skipping and chl/ exists
    if not args.skip_r:
        chl_conf_dir = project_root / "chl" / "comunas" / "conf"
        if chl_conf_dir.exists():
            print("Copying R files from chl/comunas/conf/...")
            # Copy config.R
            if (chl_conf_dir / "config.R").exists():
                if copy_file(
                    chl_conf_dir / "config.R", test_data_dir / "conf" / "config.R", args.force
                ):
                    stats["r_files_copied"] += 1
            # Copy functions.R
            if (chl_conf_dir / "functions.R").exists():
                if copy_file(
                    chl_conf_dir / "functions.R", test_data_dir / "conf" / "functions.R", args.force
                ):
                    stats["r_files_copied"] += 1
        else:
            print("  WARNING: chl/comunas/conf/ does not exist, skipping R files")

    # Copy config files from data/conf/
    conf_src_dir = project_root / "data" / "conf"
    conf_dst_dir = test_data_dir / "conf"
    if conf_src_dir.exists():
        print("\nCopying configuration files from data/conf/...")
        stats["conf_files_copied"] = copy_directory(conf_src_dir, conf_dst_dir, args.force)
    else:
        print("  ERROR: data/conf/ does not exist")
        return 1

    # Copy layer files from data/layers/csv/
    layers_src_dir = project_root / "data" / "layers" / "csv"
    layers_dst_dir = test_data_dir / "layers"
    if layers_src_dir.exists():
        print("\nCopying layer files from data/layers/csv/...")
        stats["layer_files_copied"] = copy_directory(layers_src_dir, layers_dst_dir, args.force)
    else:
        print("  ERROR: data/layers/csv/ does not exist")
        return 1

    # Copy layers.csv
    layers_csv_src = project_root / "data" / "layers.csv"
    layers_csv_dst = test_data_dir / "layers.csv"
    if layers_csv_src.exists():
        print("\nCopying layers.csv...")
        if copy_file(layers_csv_src, layers_csv_dst, args.force):
            stats["layer_csv_copied"] = True
    else:
        print("  ERROR: data/layers.csv does not exist")
        return 1

    # Create config.yaml
    print("\nCreating config.yaml for Python...")
    if create_config_yaml(test_data_dir):
        stats["config_yaml_created"] = True

    # Print summary
    print("\n" + "=" * 70)
    print("Summary")
    print("=" * 70)
    print(f"R files copied: {stats['r_files_copied']}")
    print(f"Config files copied: {stats['conf_files_copied']}")
    print(f"Layer files copied: {stats['layer_files_copied']}")
    print(f"Layers.csv copied: {stats['layer_csv_copied']}")
    print(f"Config.yaml created: {stats['config_yaml_created']}")
    print("=" * 70)

    # Exit with success
    return 0


if __name__ == "__main__":
    exit(main())
