#!/usr/bin/env python3
"""
Setup Test Data Script

Copies test data from source directories to tests/fixtures/test_data/ for testing purposes.
Supports --skip-r flag to skip R-specific files and --force flag to overwrite existing files.

Usage:
    python tests/scripts/setup_test_data.py [--skip-r] [--force]
"""

import argparse
import shutil
from pathlib import Path


def copy_file(src: Path, dst: Path, force: bool = False) -> bool:
    """Copy a file from src to dst.

    Args:
        src: Source file path
        dst: Destination file path
        force: Overwrite existing file

    Returns:
        True if file was copied, False otherwise
    """
    if dst.exists() and not force:
        print(f"  SKIP: {dst.name} (already exists, use --force to overwrite)")
        return False

    try:
        dst.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src, dst)
        print(f"  COPIED: {dst.name}")
        return True
    except Exception as e:
        print(f"  ERROR copying {src.name}: {e}")
        return False


def copy_directory(src: Path, dst: Path, force: bool = False) -> int:
    """Copy all files from src directory to dst directory.

    Args:
        src: Source directory path
        dst: Destination directory path
        force: Overwrite existing files

    Returns:
        Number of files copied
    """
    if not src.exists():
        print(f"  ERROR: Source directory does not exist: {src}")
        return 0

    count = 0
    for file in src.iterdir():
        if file.is_file():
            if copy_file(file, dst / file.name, force):
                count += 1
    return count


